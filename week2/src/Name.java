public class Name {

	String firstname;
	String lastname;

	public Name(String firstName, String lastName) {
		// super();
		firstname = firstName;
		lastname = lastName;
	}

	public String getFirstName() {
		return firstname;
	}

	public void setFirstName(String firstName) {
		firstname = firstName;
	}

	public String getLastName() {
		return lastname;
	}

	public void setLastName(String lastName) {
		lastname = lastName;
	}

}
